#!/usr/bin/env node
/**
 * sync-changelog.mjs
 *
 * Reads CHANGELOG.md at the repo root and writes its content into
 * src/constants/changelog.ts as:
 *   - CHANGELOG: raw markdown string (kept for backwards compat / tests)
 *   - CHANGELOG_ENTRIES: structured ChangelogEntry[] for native rendering
 *
 * Usage:
 *   node scripts/sync-changelog.mjs
 *   npm run sync-changelog
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

const mdPath = resolve(root, 'CHANGELOG.md');
const outPath = resolve(root, 'src', 'constants', 'changelog.ts');

const content = readFileSync(mdPath, 'utf8');

// ── Raw string (existing behaviour) ────────────────────────────────────────
const escaped = content.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');

// ── Structured parser ───────────────────────────────────────────────────────
/**
 * Parse CHANGELOG.md into ChangelogEntry[].
 *
 * Expected format (Keep a Changelog):
 *   ## [Unreleased]
 *   _No unreleased changes yet._
 *
 *   ## [1.0.0] - 2026-04-23
 *   ### Added
 *   - item one
 *   - item two
 */
function parseChangelog(text) {
  const lines = text.split('\n');
  const entries = [];
  let current = null;
  let currentSection = null;

  for (const raw of lines) {
    const line = raw.trimEnd();

    // Reference-style link at bottom — skip
    if (/^\[.+\]:\s+https?:\/\//.test(line)) continue;

    // Horizontal rule — skip
    if (/^---+$/.test(line)) continue;

    // Version heading — support any heading level (## through #####)
    const versionMatch = line.match(/^#{2,5} \[(.+?)\](?:\s+-\s+(\d{4}-\d{2}-\d{2}))?/);
    if (versionMatch) {
      if (current) entries.push(current);
      currentSection = null;
      current = {
        version: versionMatch[1],
        date: versionMatch[2] ?? null,
        sections: [],
      };
      continue;
    }

    if (!current) continue;

    // Subsection heading — support any heading level (### through ######)
    const subMatch = line.match(/^#{3,6} (.+)/);
    if (subMatch) {
      currentSection = { title: subMatch[1], items: [] };
      current.sections.push(currentSection);
      continue;
    }

    // Bullet item
    const bulletMatch = line.match(/^- (.+)/);
    if (bulletMatch) {
      const text = bulletMatch[1];
      if (currentSection) {
        currentSection.items.push(text);
      } else {
        // Bullets without a subsection — put in implicit section
        if (!current.sections.length || current.sections[current.sections.length - 1].title !== '') {
          currentSection = { title: '', items: [] };
          current.sections.push(currentSection);
        }
        currentSection.items.push(text);
      }
      continue;
    }

    // Italic-only placeholder line, e.g. _No unreleased changes yet._
    const emptyNoteMatch = line.match(/^_(.+)_$/);
    if (emptyNoteMatch && current) {
      current.emptyNote = emptyNoteMatch[1];
      continue;
    }
  }

  if (current) entries.push(current);
  return entries;
}

const entries = parseChangelog(content);
const entriesJson = JSON.stringify(entries, null, 2)
  // Indent each line of JSON by 2 more spaces so it looks clean inside the const
  .replace(/^/gm, '  ')
  .trimStart();

const output = `/**
 * In-app changelog string.
 *
 * This file is generated from CHANGELOG.md by:
 *   node scripts/sync-changelog.mjs
 *
 * Run that script (or \`npm run sync-changelog\`) before committing changes to
 * CHANGELOG.md so the in-app About screen stays up to date.
 *
 * DO NOT edit this file by hand — edit CHANGELOG.md instead.
 */

export type ChangelogSection = { title: string; items: string[] };

export type ChangelogEntry = {
  version: string;
  date: string | null;
  sections: ChangelogSection[];
  emptyNote?: string;
};

export const CHANGELOG_ENTRIES: ChangelogEntry[] = ${entriesJson};

export const CHANGELOG = \`${escaped}\`;
`;

writeFileSync(outPath, output, 'utf8');
console.log(`✓ Synced CHANGELOG.md → src/constants/changelog.ts (${content.length} chars, ${entries.length} entries)`);
