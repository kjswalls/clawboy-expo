#!/usr/bin/env node
/**
 * check-changelog-synced.mjs
 *
 * Verifies that src/constants/changelog.ts is up to date with CHANGELOG.md.
 * Regenerates the expected output in memory and compares it to the file on disk.
 * Run automatically via the `pretest` npm script.
 *
 * Exit 0 if in sync. Exit 1 with an actionable message if not.
 */

import { readFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const mdPath = resolve(root, 'CHANGELOG.md');
const outPath = resolve(root, 'src', 'constants', 'changelog.ts');

const content = readFileSync(mdPath, 'utf8');
const current = readFileSync(outPath, 'utf8');

// ── Replicate generation from sync-changelog.mjs ───────────────────────────────

const escaped = content.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');

function parseChangelog(text) {
  const lines = text.split('\n');
  const entries = [];
  let current = null;
  let currentSection = null;

  for (const raw of lines) {
    const line = raw.trimEnd();

    if (/^\[.+\]:\s+https?:\/\//.test(line)) continue;
    if (/^---+$/.test(line)) continue;

    const versionMatch = line.match(/^#{2,5} \[(.+?)\](?:\s+-\s+(\d{4}-\d{2}-\d{2}))?/);
    if (versionMatch) {
      if (current) entries.push(current);
      currentSection = null;
      current = { version: versionMatch[1], date: versionMatch[2] ?? null, sections: [] };
      continue;
    }

    if (!current) continue;

    const subMatch = line.match(/^#{3,6} (.+)/);
    if (subMatch) {
      currentSection = { title: subMatch[1], items: [] };
      current.sections.push(currentSection);
      continue;
    }

    const bulletMatch = line.match(/^- (.+)/);
    if (bulletMatch) {
      const txt = bulletMatch[1];
      if (currentSection) {
        currentSection.items.push(txt);
      } else {
        if (!current.sections.length || current.sections[current.sections.length - 1].title !== '') {
          currentSection = { title: '', items: [] };
          current.sections.push(currentSection);
        }
        currentSection.items.push(txt);
      }
      continue;
    }

    const emptyNoteMatch = line.match(/^_(.+)_$/);
    if (emptyNoteMatch && current) {
      current.emptyNote = emptyNoteMatch[1];
      continue;
    }
  }

  if (current) entries.push(current);
  return entries;
}

const entries = parseChangelog(content);
const entriesJson = JSON.stringify(entries, null, 2)
  .replace(/^/gm, '  ')
  .trimStart();

const expected = `/**
 * In-app changelog string.
 *
 * This file is generated from CHANGELOG.md by:
 *   node scripts/sync-changelog.mjs
 *
 * Run that script (or \`npm run sync-changelog\`) before committing changes to
 * CHANGELOG.md so the in-app About screen stays up to date.
 *
 * DO NOT edit this file by hand — edit CHANGELOG.md instead.
 */

export type ChangelogSection = { title: string; items: string[] };

export type ChangelogEntry = {
  version: string;
  date: string | null;
  sections: ChangelogSection[];
  emptyNote?: string;
};

export const CHANGELOG_ENTRIES: ChangelogEntry[] = ${entriesJson};

export const CHANGELOG = \`${escaped}\`;
`;

// ── Compare ────────────────────────────────────────────────────────────────────

if (current !== expected) {
  console.error(
    `\n  changelog.ts is out of sync with CHANGELOG.md.\n\n` +
    `  Fix: run \`npm run sync-changelog\` then commit the updated file.\n`,
  );
  process.exit(1);
}

console.log(`check-changelog-synced: OK`);
